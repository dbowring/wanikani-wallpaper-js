var wallpaper = (function () {
var NAMESPACE = "http://www.w3.org/2000/svg";
var svg = document.createElementNS(NAMESPACE, 'svg');
svg.setAttributeNS(null, 'version', "1.1");
var width, height, font_size, x_padding, y_padding;
var x_margin, y_margin;
var font_ratio = 19 / 16.0;
var background_color = 'rgb(0, 0, 0)';
var api_base_url = 'http://www.wanikani.com/api/';
var API_KEY = '';


var background = document.createElementNS(NAMESPACE, 'rect');
background.setAttributeNS(null, 'width', '100%');
background.setAttributeNS(null, 'height', '100%');
background.setAttributeNS(null, 'fill', background_color);
svg.appendChild(background);

var levels = {
    "error": "rgb(255, 0, 0)",
    "unseen": "rgb(40, 40, 40)",
    "apprentice": "rgb(221, 0, 147)",
    "guru": "rgb(136, 45, 158)",
    "master": "rgb(41, 77, 219)",
    "enlightened": "rgb(0, 147, 221)",
    "burned": "rgb(240, 240, 240)"
};

var Char = function(index, character) {
    this.index = index;
    this.character = character;
    this.element = document.createElementNS(NAMESPACE, 'text');
    this.element.setAttribute('class', 'character');
    this.element.appendChild(document.createTextNode(character));
    this.setLevel('unseen');
};
Char.prototype.setLevel = function(level) {
    this.level = level;
    this.element.setAttributeNS(null, 'fill', levels[level] || levels.error);
};
var CharCollection = function(characters) {
    this.array = [];
    if (arguments.length == 1) {
        this.consume(characters);
    }
};
CharCollection.prototype.consume = function(characters) {
    for (var i=0; i<characters.length; ++i) {
        this.add(characters[i]);
    }
};
CharCollection.prototype.cautious_consume = function(characters) {
    for (var i=0; i<characters.length; ++i) {
        if (this.indexOf(characters[i]) < 0){
            this.add(characters[i]);
        }
    }
};
CharCollection.prototype.add = function(character) {
    var c = new Char(this.array.length, character);
    this.array.push(c);
    svg.appendChild(c.element);
    return c;
};
CharCollection.prototype.getByIndex = function(index) {
    return this.array[index];
};
CharCollection.prototype.getByCharacter = function(character) {
    return this.array[this.indexOf(character)];
};
CharCollection.prototype.indexOf = function(character) {
    for (var i=0; i<this.array.length; ++i) {
        if (this.array[i].character == character) {
            return i;
        }
    }
    return -1;
};
CharCollection.prototype.each = function(fn) {
    for (var i=0; i<this.array.length; ++i) {
        fn(i, this.array[i], this.array);
    }
};

var redraw_hold = 0;
var redraw = function() {
    if (redraw_hold > 0) {
        return;
    }
    svg.setAttributeNS(null, 'width', width);
    svg.setAttributeNS(null, 'height', height);
    var children = svg.getElementsByClassName('character');
    var font_height = font_size * font_ratio;
    var cols = Math.floor((width - (2 * x_margin) - x_padding) / (font_size + x_padding));

    svg.setAttributeNS(null, 'font-size', font_size);
    characters.each(function(index, character, characters) {
        var row = index % cols;
        var col = Math.floor(index / cols);
        character.element.setAttributeNS(null, 'x', x_margin + row * font_size + (row-1) * x_padding );
        character.element.setAttributeNS(null, 'y', y_margin + col * font_height + col * y_padding + font_height);
    });
};

var hold_redraw = function(fn, context) {
    ++redraw_hold;
    try {
        fn.call(context);
    } finally {
        --redraw_hold;
    }
};

var changeSize = function(w, h, f, px, py, mx, my) {
    width = w;
    height = h;
    font_size = f;
    x_padding = px ;
    y_padding = py;
    x_margin = mx;
    y_margin = my;
    redraw();
};


var JSONP = {};
var JSONP_INDEX = 0;
var globalize_function = function(fn, context) {
    var name = 'response_' + JSONP_INDEX;
    JSONP_INDEX += 1;
    var wrapper = function() {
        fn.apply(context, arguments);
        delete JSONP[name];
    };
    JSONP[name] = wrapper;
    return 'wallpaper.JSONP.' + name;
};

var send_request = function(url, success, error) {
    var e = document.createElement('script');
    if (error) {
        e.onerror = error;
    }
    e.type = 'text/javascript';
    e.charset = 'utf-8';
    if (success) {
        url += '?callback=' + globalize_function(success);
    }
    e.src = url;
    document.getElementsByTagName('body')[0].appendChild(e);
};

var setWidth = function(pixels) {
    width = pixels;
    redraw();
};
var setHeight = function(pixels) {
    height = pixels;
    redraw();
};
var setFontSize = function(pixels) {
    font_size = pixels;
    redraw();
};
var setXPadding = function(pixels) {
    x_padding = pixels;
    redraw();
};
var setYPadding = function(pixels) {
    y_padding = pixels;
    redraw();
};
var setXMargin = function(pixels) {
    x_margin = pixels;
    redraw();
};
var setYMargin = function(pixels) {
    y_margin = pixels;
    redraw();
};
var setAPIKey = function(key) {
    API_KEY = key;
};


var queryKanji = function() {
    if (API_KEY.length != 32) {
        alert('API KEY appears invalid (must be 32 characters long)');
        return;
    }
    var url = api_base_url + 'user/' + API_KEY + '/kanji';
    var success = function(response) {
        if (response.error) {
            return failure(response);
        }
        var card, character, info = response.requested_information;
        for (var i=0; i<info.length; ++i) {
            card = info[i];
            character = characters.getByCharacter(card.character);
            if (!character) {
                character = characters.add(card.character);
                svg.appendChild(character.element);
            }
            if (card.stats) {
                character.setLevel(card.stats.srs);
            }
        }
        redraw();
    };
    var failure = function() {
        console.log('failure');
        alert('Failed to query kanji - check your api key');
    };
    send_request(url, success, failure);
};


var emptyCharacters = function() {
    characters.each(function(i, e) {
        svg.removeChild(e.element);
    });
    characters = new CharCollection();
    redraw();
};
var AddHeisigOrder = function() {
    characters.cautious_consume(heisig_order());
    redraw();
};

var saveImage = function() {
    var s = '<svg version="1.1" xmlns="' + NAMESPACE + '" font-size="';
    s += font_size + '" width="' + width + '" height="' + height + '">';
    s += '<rect width="100%" height="100%" background="';
    s += background_color + '"></rect>';
    characters.each(function(i, e) {
        s += '<text x="';
        s += e.element.getAttributeNS(null, 'x') || '0';
        s += '" y="';
        s += e.element.getAttributeNS(null, 'y') || '0';
        s += '" fill="';
        s += e.element.getAttributeNS(null, 'fill') || '';
        s += '">' + e.character + '</text>';
    });
    s += '</svg>';
    // console.log(s);
    open('data:image/svg+xml;charset=utf-8,' + s);
};

var characters = new CharCollection(heisig_order());

return {
    changeSize: changeSize,
    setWidth: setWidth,
    setHeight: setHeight,
    setFontSize: setFontSize,
    setXPadding: setXPadding,
    setYPadding: setYPadding,
    setXMargin: setXMargin,
    setYMargin: setYMargin,
    queryKanji: queryKanji,
    setAPIKey: setAPIKey,
    redraw: redraw,
    hold_redraw: hold_redraw,
    emptyCharacters: emptyCharacters,
    AddHeisigOrder: AddHeisigOrder,
    saveImage: saveImage,
    JSONP: JSONP,
    getElement: function() {
        return svg;
    }
};

})();

